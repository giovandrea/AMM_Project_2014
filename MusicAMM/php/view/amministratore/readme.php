<h2>Informazioni sul sito</h2>

Il sito è nato inizialmente con l'idea di vendere cd musicali.
Arrivato alla parte sulle transazioni, mi sono accorto di non essere stato in grado di modificare più tabelle (cds, caratterizzazioni e artisti), non sapendo come gestire l'ultimo id inserito, e pertanto il sito è diventato un luogo dove poter noleggiare cd musicali.

